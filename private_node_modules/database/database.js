// Database module

const MongoClient = require('mongodb').MongoClient;
const mongo_url = 'mongodb://localhost:27017';
let client;

async function open_database_client() {
		// If members database doesn't exist yet, create it.
		client = await MongoClient.connect( mongo_url , {useNewUrlParser: true, useUnifiedTopology: true});
	}


async function get_database(database_name) {
		var db = await client.db(database_name);
		return db;

	}

async function close_database_client() {
	client.close();
}


module.exports = {get_database, open_database_client, close_database_client};